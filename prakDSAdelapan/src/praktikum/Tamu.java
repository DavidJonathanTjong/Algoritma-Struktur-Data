package praktikum;

public class Tamu {
    private String nama;
    private String noTelpon;
    private int tipeKamar;
    private int lamaMenginap;

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getNoTelpon() {
        return noTelpon;
    }

    public void setNoTelpon(String noTelpon) {
        this.noTelpon = noTelpon;
    }

    public int getTipeKamar() {
        return tipeKamar;
    }

    public void setTipeKamar(int tipeKamar) {
        this.tipeKamar = tipeKamar;
    }

    public int getLamaMenginap() {
        return lamaMenginap;
    }

    public void setLamaMenginap(int lamaMenginap) {
        this.lamaMenginap = lamaMenginap;
    }
    
    
}
